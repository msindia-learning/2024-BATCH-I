const Calculator = {};

Calculator.Add = function(value1, value2)
{
    return value1 + value2;
}

Calculator.Subtract = function(value1, value2)
{
    return value1 - value2;
}

Calculator.Multiply = function(value1, value2)
{
    return value1 * value2;
}

Calculator.Divide = function(value1, value2)
{
    return value1 / value2;
}

